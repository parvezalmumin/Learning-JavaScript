 // ✅ Increment Examples
let a = 5;
console.log("a++ prints:", a++); // Prints 5, then a becomes 6
console.log("Now a is:", a);     // Prints 6

let b = 5;
console.log("++b prints:", ++b); // b becomes 6, then prints 6

// ✅ Decrement Examples
let x = 10;
console.log("x-- prints:", x--); // Prints 10, then x becomes 9
console.log("Now x is:", x);     // Prints 9

let y = 10;
console.log("--y prints:", --y); // y becomes 9, then prints 9
