// Example 1: 2^3
let base = 2;
let power = 3;
let result = base ** power;

console.log(`${base} raised to the power of ${power} is:`, result); // 2 ** 3 = 8

// Example 2: 5^4
let bigResult = 5 ** 4; // 5 * 5 * 5 * 5 = 625
console.log("5 raised to the power of 4 is:", bigResult);

// Example 3: Square root using fractional exponent
let squareRoot = 16 ** 0.5; // same as Math.sqrt(16)
console.log("Square root of 16 is:", squareRoot); // 4
