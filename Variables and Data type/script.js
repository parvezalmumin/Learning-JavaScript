 // ✅ NUMBER
let age = 25;                   // Storing a number
console.log("Age is:", age);    // Output: Age is: 25

// ✅ STRING
let name = "Parvez";            // Storing text inside double quotes
console.log("Name is:", name);  // Output: Name is: Parvez

// ✅ BOOLEAN
let isStudent = true;                     // true or false (yes/no type logic)
console.log("Is student:", isStudent);    // Output: Is student: true

// ✅ UNDEFINED
let futureJob;                  // Declared but not assigned a value
console.log("Future job is:", futureJob); // Output: Future job is: undefined

